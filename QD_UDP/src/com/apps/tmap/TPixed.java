package com.apps.tmap;

public class TPixed {
	private double x;
    private double y;
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public TPixed(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public TPixed() {
		super();
	}
	public String toString() {
		return super.toString();
	}
    
    
}
